package control;

import insuranceRegistration.carInsuranceRegistration;

public class cCarInsuranceRegistration {
	private carInsuranceRegistration carInsuranceRegistration;

	public cCarInsuranceRegistration() {
		this.carInsuranceRegistration = new carInsuranceRegistration();
	}
	
	public void run() {
		carInsuranceRegistration.run();
	}


	public void setName(String name) {
		carInsuranceRegistration.setName(name);
	}


	public void setResidentRegistrationNumber(String residentRegistrationNumber) {
		carInsuranceRegistration.setResidentRegistrationNumber(residentRegistrationNumber);
	}


	public void setAge(int age) {
		carInsuranceRegistration.setAge(age);
	}


	public void setPhoneNumber(String phoneNumber) {
		carInsuranceRegistration.setPhoneNumber(phoneNumber);
	}


	public void setAddress(String address) {
		carInsuranceRegistration.setAddress(address);
	}


	public void setAccountNumber(String accountNumber) {
		carInsuranceRegistration.setAccountNumber(accountNumber);
	}

	public void setNote(String note) {
		carInsuranceRegistration.setNote(note);
	}
	public void setGender(String gender) {
		carInsuranceRegistration.setGender(gender);
	}

	public void setJob(String job) {
		carInsuranceRegistration.setJob(job);
	}

	public void setLongterm(String longterm) {
		carInsuranceRegistration.setLongterm(longterm);
	}

	public void setCarType(String carType) {
		carInsuranceRegistration.setCarType(carType);
	}

	public void setSelectCarType(String selectCarType) {
		carInsuranceRegistration.setSelectCarType(selectCarType);
	}
}
